public class Node {

	// private class variables
	private int x;
	private int y;
	
	// constructor
	public Node(int x, int y)
	{
		this.x = x;
		this.y = y;
	}
	
	// accessor/getter method
	public int getX()
	{
		return this.x;
	}
	
	// accessor/getter method
	public int getY()
	{
		return this.y;
	}
	
}
